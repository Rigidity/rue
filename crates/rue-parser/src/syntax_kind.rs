use std::fmt;

use num_derive::{FromPrimitive, ToPrimitive};

#[derive(
    Debug, Default, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, ToPrimitive, FromPrimitive,
)]
pub enum SyntaxKind {
    #[default]
    Eof,
    Error,

    Ident,
    Int,
    Hex,
    String,

    OpenParen,
    CloseParen,
    OpenBracket,
    CloseBracket,
    OpenBrace,
    CloseBrace,

    Fun,
    Inline,
    Import,
    Export,
    Type,
    Struct,
    Enum,
    Let,
    Const,

    If,
    Else,
    Return,
    Raise,
    Assert,
    Nil,
    True,
    False,
    As,
    Is,

    Dot,
    Comma,
    Colon,
    PathSeparator,
    Semicolon,
    Arrow,
    FatArrow,
    Spread,
    Question,

    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Not,
    LessThan,
    GreaterThan,
    LessThanEquals,
    GreaterThanEquals,
    Equals,
    NotEquals,
    Assign,

    And,
    Or,

    Whitespace,
    LineComment,
    BlockComment,

    Root,
    FunctionItem,
    FunctionParam,
    TypeAliasItem,
    StructItem,
    StructField,
    EnumItem,
    EnumVariant,
    ConstItem,
    ImportItem,
    ImportPath,
    ImportGroup,

    LetStmt,
    IfStmt,
    ReturnStmt,
    RaiseStmt,
    AssertStmt,

    Block,
    Path,

    InitializerExpr,
    InitializerField,
    LiteralExpr,
    ListExpr,
    ListItem,
    PairExpr,
    LambdaExpr,
    LambdaParam,
    PrefixExpr,
    BinaryExpr,
    GroupExpr,
    CastExpr,
    GuardExpr,
    IfExpr,
    FunctionCall,
    FunctionCallArg,
    FieldAccess,
    IndexAccess,

    ListType,
    ListTypeItem,
    PairType,
    FunctionType,
    FunctionTypeParam,
    OptionalType,
}

impl fmt::Display for SyntaxKind {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(
            f,
            "{}",
            match self {
                SyntaxKind::Eof => "end of file",
                SyntaxKind::Error => "error",

                SyntaxKind::Ident => "identifier",
                SyntaxKind::Int => "integer literal",
                SyntaxKind::Hex => "hex literal",
                SyntaxKind::String => "string",

                SyntaxKind::OpenParen => "'('",
                SyntaxKind::CloseParen => "')'",
                SyntaxKind::OpenBracket => "'['",
                SyntaxKind::CloseBracket => "']'",
                SyntaxKind::OpenBrace => "'{'",
                SyntaxKind::CloseBrace => "'}'",

                SyntaxKind::Fun => "'fun'",
                SyntaxKind::Inline => "'inline'",
                SyntaxKind::Import => "'import'",
                SyntaxKind::Export => "'export'",
                SyntaxKind::Type => "'type'",
                SyntaxKind::Struct => "'struct'",
                SyntaxKind::Enum => "'enum'",
                SyntaxKind::Let => "'let'",
                SyntaxKind::Const => "'const'",

                SyntaxKind::If => "'if'",
                SyntaxKind::Else => "'else'",
                SyntaxKind::Return => "'return'",
                SyntaxKind::Raise => "'raise'",
                SyntaxKind::Assert => "'assert'",
                SyntaxKind::Nil => "'nil'",
                SyntaxKind::True => "'true'",
                SyntaxKind::False => "'false'",
                SyntaxKind::As => "'as'",
                SyntaxKind::Is => "'is'",

                SyntaxKind::Dot => "'.'",
                SyntaxKind::Comma => "','",
                SyntaxKind::Colon => "':'",
                SyntaxKind::PathSeparator => "'::'",
                SyntaxKind::Semicolon => "';'",
                SyntaxKind::Arrow => "'->'",
                SyntaxKind::FatArrow => "'=>'",
                SyntaxKind::Spread => "'...'",
                SyntaxKind::Question => "'?'",

                SyntaxKind::Plus => "'+'",
                SyntaxKind::Minus => "'-'",
                SyntaxKind::Star => "'*'",
                SyntaxKind::Slash => "'/'",
                SyntaxKind::Percent => "'%'",
                SyntaxKind::Not => "'!'",
                SyntaxKind::LessThan => "'<'",
                SyntaxKind::GreaterThan => "'>'",
                SyntaxKind::LessThanEquals => "'<='",
                SyntaxKind::GreaterThanEquals => "'>='",
                SyntaxKind::Equals => "'=='",
                SyntaxKind::NotEquals => "'!='",
                SyntaxKind::Assign => "'='",

                SyntaxKind::And => "'&&'",
                SyntaxKind::Or => "'||'",

                SyntaxKind::Whitespace => "whitespace",
                SyntaxKind::LineComment => "line comment",
                SyntaxKind::BlockComment => "block comment",

                SyntaxKind::Root => "root",
                SyntaxKind::FunctionItem => "function item",
                SyntaxKind::FunctionParam => "function param",
                SyntaxKind::TypeAliasItem => "type alias item",
                SyntaxKind::StructItem => "struct item",
                SyntaxKind::StructField => "struct field",
                SyntaxKind::EnumItem => "enum item",
                SyntaxKind::EnumVariant => "enum variant",
                SyntaxKind::ConstItem => "const item",
                SyntaxKind::ImportItem => "import item",
                SyntaxKind::ImportPath => "import path",
                SyntaxKind::ImportGroup => "import group",

                SyntaxKind::LetStmt => "let statement",
                SyntaxKind::IfStmt => "if statement",
                SyntaxKind::ReturnStmt => "return statement",
                SyntaxKind::RaiseStmt => "raise statement",
                SyntaxKind::AssertStmt => "assert statement",

                SyntaxKind::Block => "block",
                SyntaxKind::Path => "identifier path",

                SyntaxKind::InitializerExpr => "initializer expression",
                SyntaxKind::InitializerField => "initializer field",
                SyntaxKind::LiteralExpr => "literal expression",
                SyntaxKind::ListExpr => "list expression",
                SyntaxKind::ListItem => "list item",
                SyntaxKind::PairExpr => "pair expression",
                SyntaxKind::LambdaExpr => "lambda expression",
                SyntaxKind::LambdaParam => "lambda param",
                SyntaxKind::PrefixExpr => "prefix expression",
                SyntaxKind::BinaryExpr => "binary expression",
                SyntaxKind::GroupExpr => "group expression",
                SyntaxKind::CastExpr => "cast expression",
                SyntaxKind::GuardExpr => "guard expression",
                SyntaxKind::IfExpr => "if expression",
                SyntaxKind::FunctionCall => "function call",
                SyntaxKind::FunctionCallArg => "function call argument",
                SyntaxKind::FieldAccess => "field access",
                SyntaxKind::IndexAccess => "index access",

                SyntaxKind::ListType => "list type",
                SyntaxKind::ListTypeItem => "list type item",
                SyntaxKind::PairType => "pair type",
                SyntaxKind::FunctionType => "function type",
                SyntaxKind::FunctionTypeParam => "function type parameter",
                SyntaxKind::OptionalType => "optional type",
            }
        )
    }
}
